#----ESTRUCTURA DE DATOS-----

#---------STRING--------

#* Es una secuencia de caracteres.
#- Estructura de Datos: Secuencia (inmutable) 
# ('') o ("")
#  Ej:  "Hola, mundo "

#---------LISTS--------

#* Colección ordenada y mutable de elementos.
#- Estructura de Datos: Secuencia (mutable)
#  [ ]
#  Ej:  lista = [ 1, 2, 3, 4]   

#---------TUPLES--------

#* Similar a lista pero inmutable.
#- Estructura de Datos: Secuencia (inmutable)
#  ( )
#  Ej:  tupla = (1, 2, 3, 4)  

#---------DICTIONARIES--------

#* Colección no ordenada de clave-valor. 
#- Estructura de Datos: Mapeo (mutable)
#  { }
#  Ej:  Diccionario = {"clave1":"valor1","clave2":"valor2"}  


